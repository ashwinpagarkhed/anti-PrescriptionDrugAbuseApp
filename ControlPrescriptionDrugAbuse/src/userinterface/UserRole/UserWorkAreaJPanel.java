    private void btnGetPrescriptionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGetPrescriptionActionPerformed
        // TODO add your handling code here:
        int selectedRow = userWorkRequestJTable.getSelectedRow();
        
        if (selectedRow >= 0) {
            DoctorAccount drUserAccount = (DoctorAccount) userWorkRequestJTable.getValueAt(selectedRow, 0);
            Prescription prescription = new Prescription();
            prescription.setDoctor(drUserAccount);
            prescription.setPatient(userAccount);            
            userAccount.addPrescription(prescription);
  
            
            dB4OUtil.storeSystem(system);
            
            CardLayout layout=(CardLayout)userProcessContainer.getLayout();
            userProcessContainer.add(new PrescriptionJPanel(userProcessContainer, userAccount, system));
            layout.next(userProcessContainer);
        } 
        else {
            JOptionPane.showMessageDialog(null, "Please select a Doctor.");
        }
        
    }
    private void populateTable() {
       DefaultTableModel dtm = (DefaultTableModel) userWorkRequestJTable.getModel();
       dtm.setRowCount(0);

        for(Network network: system.getNetworkList()){
            if(network.getName().equals(userAccount.getNetwork().getName())) {
                for(Enterprise enterprise: network.getEnterpriseDirectory().getEnterpriseList()) {
                    if(enterprise.getEnterpriseType() == Enterprise.EnterpriseType.GoverningBody) {
                        for(Organization organization: enterprise.getOrganizationDirectory().getOrganizationList()) {
                            if(organization instanceof DoctorOrganization){
                                for (UserAccount drUserAccount : organization.getUserAccountDirectory().getUserAccountList()) {
                                    DoctorAccount dr = (DoctorAccount) drUserAccount;
                                    if(dr.isLicense()) {
                                        Object row[] = new Object[2];
                                        row[0] = drUserAccount;
                                        row[1] = dr.isLicense();
                                        dtm.addRow(row);
                                    }
                                }
                            }
                        }
                    }
                }
            }
        }
    }//GEN-LAST:event_btnGetPrescriptionActionPerformed

