    private void populateStoreTable(){
        DefaultTableModel dtm = (DefaultTableModel) workRequestJTable.getModel();
        dtm.setRowCount(0);
        
        for(WorkRequest workRequest : userAccount.getWorkQueue().getWorkRequestList()) {
            PrescriptionRequest prescriptionWorkRequest = (PrescriptionRequest) workRequest;
            if(prescriptionWorkRequest.getPrescription().getPatient() != null){
                if(prescriptionWorkRequest.getPrescription().getPatient().equals(userAccount)){
                    StoreAccount storeAccount = (StoreAccount) prescriptionWorkRequest.getStoreAccount();

                    String storeAddress = "";
                    if(storeAccount != null) {
                        storeAddress = storeAccount.getAddress();
                    }

                    Object row[] = new Object[4];
                    row[0] = prescriptionWorkRequest;
                    row[1] = storeAddress;
                    row[2] = prescriptionWorkRequest.getStatus();
                    row[3] = Helper.getCurrentDateTimeFromDateObject(prescriptionWorkRequest.getRequestDate());

                    dtm.addRow(row);  
                }
            }
        }
    }

